#include "do_op.h"

void	ft_putnbr(int nb)
{
	long	n;
	char	tmp;

	n = nb;
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	tmp = (n % 10) + '0';
	write(1, &tmp, 1);
}
