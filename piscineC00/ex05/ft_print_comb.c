#include <unistd.h>

void	ft_print_comb(void)
{
	char	n[3];

	n[0] = '0';
	n[1] = '1';
	n[2] = '2';
	while (1)
	{
		write(1, &n, 3);
		if (n[0] == '7')
			return ;
		write(1, ", ", 2);
		if (n[2] < '9')
			n[2]++;
		else
		{
			if (n[1] < '0')
				n[1]++;
			else
			{
				n[0]++;
				n[1] = n[0] + 1;
			}
			n[2] = n[1] + 1;
		}
	}
}
